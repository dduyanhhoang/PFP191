import re

def is_valid_number(phone_number):
    pattern = r'^((032|033|034|035|036|037|038|039|096|097\
                    |098|086|083|084|085|081|082|088|091|094\
                    |070|079|077|076|078|090|093|089|056|058\
                    |092|059|099)[0-9]{7})\
                |(\+84(32|33|34|35|36|37|38|39|96|97\
                    |98|86|83|84|85|81|82|88|91|94\
                    |70|79|77|76|78|90|93|89|56|58\
                    |92|59|99)[0-9]{7})$'
    
    if re.fullmatch(pattern, phone_number):
        return True
    else:
        return False

numbers = ["0123456789", "0912345678", "+84912345678", "123456", "084123456789"]
for number in numbers:
    print(f"{number}: {is_valid_number(number)}")
